package com.example.authservice.authentication;

import javax.persistence.*;

@Entity
public class Authentication {
    @Id
    private Integer id;

    @Column(nullable = false, length = 20, unique = true)
    private String username;

    @Column(nullable = false, length = 60)
    private String password;

    public enum Role {
        USER,
        ADMIN
    }

    @Enumerated(EnumType.ORDINAL)
    @Column(name="role", columnDefinition="TINYINT default 0 COMMENT '0 for USER, 1 for ADMIN'")
    private Role role;

    public Authentication() {
    }

    public Authentication(Integer id, String username, String password, Role role) {
        this.id = id;
        this.username = username;
        this.password = password;
        this.role = role;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public Role getRole() {
        return role;
    }

    public void setRole(Role role) {
        this.role = role;
    }
}
